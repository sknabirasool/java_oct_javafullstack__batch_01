package org.javacourse.dataTypes;

public class TypeCasting {
    public static void main(String[] args) {
        int i=100;
        //convertiong into long
        long l=i;
        float f=l;
        System.out.println("int "+i);
        System.out.println("long "+l);
        System.out.println("float "+f);
    }
}
